package Variables;

public class TypesVariable {
//global variable
    int b = 20; // b is a global variable, defined at class level

    //static variable
    static int c = 30; //c is a static variable because it is prefixed with keyword static


    public static void main(String[] args) {
        //local variable: defined in its block
        int a = 10; // a is a local variable

    }
}
